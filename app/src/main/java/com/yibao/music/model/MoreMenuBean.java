package com.yibao.music.model;


public class MoreMenuBean {
    private int picId;
    private int nameId;

    public MoreMenuBean(int picId, int nameId) {
        this.picId = picId;
        this.nameId = nameId;
    }

    public int getPicId() {
        return picId;
    }


    public int getNameId() {
        return nameId;
    }

}
